// Helper function to determine brand from console name
export function getConsoleBrand(name: string): ConsoleBrand {
  if (name.includes('Nintendo')) return 'Nintendo';
  if (name.includes('Sony') || name.includes('Playstation') || name.includes('PSP') || name.includes('PS Vita')) return 'Sony';
  if (name.includes('Sega')) return 'Sega';
  if (name.includes('Microsoft') || name.includes('Xbox')) return 'Microsoft';
  if (name.includes('Atari')) return 'Atari';
  if (name.includes('SNK') || name.includes('Neo Geo')) return 'SNK';
  if (name.includes('NEC') || name.includes('PC Engine') || name.includes('TurboGrafx')) return 'NEC';
  if (name.includes('Commodore')) return 'Commodore';
  return 'Other';
}

